package model;

import java.util.ArrayList;

public class ContenuFournitures {
	private ArrayList<Integer> idContenuFourniture = new ArrayList<Integer>();
	private ArrayList<Integer> idProduitsFourniture = new ArrayList<Integer>();
	private ArrayList<Integer> quantiteProduitsFourniture = new ArrayList<Integer>();
	
	public ArrayList<Integer> getIdContenuFourniture() {
		return idContenuFourniture;
	}
	public ArrayList<Integer> getIdProduitsFourniture() {
		return idProduitsFourniture;
	}
	public ArrayList<Integer> getQuantiteProduitsFourniture() {
		return quantiteProduitsFourniture;
	}
	
}
